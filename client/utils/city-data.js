// ============ 搜索城市（支持模糊匹配） ============
function searchCities(keyword) {
  if (!keyword || keyword.length < 1) return []
  const kw = keyword.toLowerCase()
  const results = []

  // 搜中国城市
  for (const province in CHINA_CITIES) {
    // 省名匹配
    if (province.toLowerCase().includes(kw)) {
      CHINA_CITIES[province].forEach(city => {
        results.push({ label: province + '·' + city, province, city, type: 'china' })
      })
      continue
    }
    // 城市名匹配
    CHINA_CITIES[province].forEach(city => {
      if (city.toLowerCase().includes(kw)) {
        results.push({ label: province + '·' + city, province, city, type: 'china' })
      }
    })
  }

  // 搜国际城市
  for (const country in INTERNATIONAL_CITIES) {
    const regions = INTERNATIONAL_CITIES[country]
    // 国家名匹配 — 只返回每个地区的前两个城市
    if (country.toLowerCase().includes(kw)) {
      for (const region in regions) {
        regions[region].slice(0, 2).forEach(city => {
          results.push({ label: country + '·' + city, country, region, city, type: 'international' })
        })
      }
      continue
    }
    // 城市名匹配
    for (const region in regions) {
      regions[region].forEach(city => {
        if (city.toLowerCase().includes(kw)) {
          results.push({ label: country + '·' + city, country, region, city, type: 'international' })
        }
      })
    }
  }

  return results.slice(0, 30) // 最多返回30个结果
}

